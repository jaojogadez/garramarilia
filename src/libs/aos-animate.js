// Script para inicializar a biblioteca AOS (Animate On Scroll)

AOS.init({
  // Define o offset (em px) de quando a animação deve começar
  offset: 120,
  // Duração da animação (em ms)
  duration: 1000,
  // Uma vez que o elemento é animado, se deve ou não repetir a animação ao rolar para cima
  once: true,
});
